#include "AddressLibrary.h"

class IRTTI
{
private:
	inline static constexpr std::uint32_t NUM_RTTI = 3279ui32;

	using rdc_func_type = void* (*)(const void*, std::uint32_t, void*, void*, std::uint32_t);

	template <class T>
	friend struct RTTI;

	IRTTI();

	static inline const auto rdc_func = IAL::Address<rdc_func_type>(102238, 109689);

	static std::uint32_t aids[NUM_RTTI];
	static std::uint32_t aids_ae[NUM_RTTI];
	static void* addrs[NUM_RTTI];

	static IRTTI m_Instance;
};

template <class T>
struct RTTI
{
private:
	using to_type = stl::strip_type<T>;

public:
	RTTI() = default;

	template <class Tf, class from_type = stl::strip_type<Tf>>
	static inline constexpr to_type* Cast(const Tf* a_from)
	{
		return reinterpret_cast<to_type*>(IRTTI::rdc_func(
			reinterpret_cast<const void*>(a_from),
			0,
			IRTTI::addrs[stl::underlying(from_type::RTTI_ID)],
			IRTTI::addrs[stl::underlying(to_type::RTTI_ID)],
			0));
	}

	template <class Tf>
	inline constexpr to_type* operator()(const Tf* a_from)
	{
		return Cast<Tf>(a_from);
	}
};
